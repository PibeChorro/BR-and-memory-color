function [design] = designSettings(language,design)

if nargin < 2
    design = struct;
    
    design.stimulusPresentationTime = 120;
    design.ITI                      = 10;
    design.numTrials                = 5;
end

if strcmp (language, 'german')
    % general
    design.Introduction = [
        'Vielen Dank dass du an unserer Binocular Rivalry Studie teilnimmst.\n\n'...
        'Drücke eine beliebige Taste um fortzufahren'
        ];
    % flicker method to get isoluminant stimuli
    design.isolumInstruction = ['HELLIGKEITS ANPASSUNG\n\n' ...
        'Im Folgenden wirst du eine Reihe\n' ...
        'farbiger, flackernder Rechtecke sehen,\n' ...
        'Deine Aufgabe ist es, \n' ...
        'mit den Tasten "4" und "6" die Helligkeit so zu ändern,\n'...
        'dass die Rechtecke so wenig\n ' ...
        'flackern wie möglich.' ...
        'Wenn du glaubst das Flackern\n' ...
        'minimiert zu haben, drücke die "5" Taste.\n\n' ...
        'Diese Aufgabe wird 15 Mal wiederholt und dauert ca. 5 Minuten.\n\n' ...
        'DRÜCKE DIE "5" UM ZU STARTEN'];
    % BR grating experiment
    design.InstructionGratings1 = [
        'Im Folgenden wirst du farbige Gitter,\n' ...
        'gleichzeitig auf beide Augen präsentiert bekommen.\n' ...
        'Die Gitter haben unterschiedliche Farben und sind im rechten Winkel zueinander.\n' ...
        'Deine Wahrnehmung wird zwischen den beiden Gittern hin und her wechseln.\n' ...
        'Deine Aufgabe wird sein durch Tastendrücke zu zeigen welches Gitter du im\n' ...
        'Moment wahr nimmst. Solltest du eine Mischung beider Gitter wahr nehmen, drücke bitte KEINE Taste\n' ...
        'Bitte halte die Taste so lange gedrückt wie du das Gitter wahr nimmst.\n\n'...
        'Drücke eine beliebige Taste um fort zu fahren'];
    design.InstructionGratings2 = [
        'In jedem Durchlauf wirst du ' num2str(design.numTrials) ' Gitterpaare (' num2str(design.numTrials) ' trials) sehen.\n' ...
        'Jedes Paar ist für ' num2str(round(design.stimulusPresentationTime)) 's zu sehen.\n' ...
        'Zwischen den trials gibt es eine Pause von ' num2str(round(design.ITI)) 's.\n\n'...
        'Drücke eine beliebige Taste um fort zu fahren'
        ];
    design.InstructionGratings3 = [
        'Erinnerung:\n\n' ...
        num2str(design.numTrials) ' Gitterpaare (trials)\n' ...
        'Jedes Gitterpaar wird für ' num2str(round(design.stimulusPresentationTime)) 's gezeigt.\n' ...
        'Zwischen den trials gibt es eine Pause von ' num2str(round(design.ITI)) 's.\n\n' ...
        'Wenn du ein Gitter dominant wahr nimmst, halte die entsprechende Taste gedrückt\n' ...
        'Wenn du eine Mischung der Gitter wahr nimmst, drücke KEINE Taste\n' ...
        'Versuch während des gesamten Experiments aufmerksam zu sein.\n\n' ...
        'Drücke eine beliebige Taste um fort zu fahren'
    ];
    design.trueExampleText = 'Wenn du dieses Muster\n wahr nimmst, drücke bitte die 4';
    design.invExampleText = 'Wenn du dieses Muster\n wahr nimmst, drücke bitte die 6';
    design.proceedText = 'Drücke eine beliebige Taste um fort zu fahren';
    
    % BR object experiment
    design.Instruction1 = [
        'Im Folgenden wirst du Objekte in ihrer typischen Farbe\n' ...
        'und in untypischer Farbe gleichzeitig auf beide Augen präsentiert bekommen.\n' ...
        'Deine Wahrnemung der Objekte wird zwischen den beiden Farben ständig wechseln.\n' ...
        'Wenn du das Objekt in seiner typischen Farbe siehst, drücke bitte die linke (4) Taste.\n' ...
        'Siehst du das Objekt in einer untypischen Farbe, drücke bitte die rechte (6) Taste.\n' ...
        'Bitte halte die Taste so lange gedrückt wie du die entsprechende Farbe wahr nimmst.\n' ...
        'Solltest du eine Mischung beider Farben oder das Object zum Teil in der einen und\n' ...
        'zum Teil in der anderen Farbe sehen, drücke bitte KEINE Taste.\n\n' ...
        'Drücke eine beliebige Taste um fort zu fahren'
        ];
    design.Instruction2 = [
        'In jedem Durchlauf siehst du ' num2str(design.numTrials) ' verschiedene Objekte (' num2str(design.numTrials) ' trials).\n' ...
        'Jedes Objekt wird für ' num2str(round(design.stimulusPresentationTime)) 's gezeigt.\n' ...
        'Zwischen den trials gibt es eine Pause von ' num2str(round(design.ITI)) 's.\n\n' ...
        'Versuch während des gesamten Experiments aufmerksam zu sein.\n\n' ...
        'Drücke eine beliebige Taste um fort zu fahren'
        ];
    design.Instruction3 = [
        'Erinnerung:\n\n' ...
        'LINKS (4) für Objetk in typischer Farbe\n' ...
        'RECHTS (6) für Objekt in untypischer Farbe\n\n' ...
        'Die entsprechende Taste gedrückt halten.\n' ...
        'Bei nicht eindeutiger Wahrnehmung KEINE Taste drücken.\n' ...
        num2str(round(design.stimulusPresentationTime)) 's pro Objekt, ' num2str(round(design.ITI)) 's Pause zwischen den Objekten.\n\n' ...
        'Drücke eine beliebige Taste um fort zu fahren'
        ];
    design.waitTillStart = [
        'Wir starten in ' num2str(round(design.ITI)) 's.\n\n' 
        ];
    
    design.FusionText   = [
        'Fusionstest: nutze die Pfeiltasten, um das Feld einzustellen. \n'...
        'Drücke "ENTER" um zu bestätigen'];
    
    design.RunIsOver = [
        'Der Durchlauf ist vorbei.\n\n' ...
        'Drücke eine beliebige Taste und wende dich an den Versuchsleiter'
        ];
else
    %% English version of instructions
    % general
    design.Introduction = [
        'Thank you very much for participating in our study.\n\n'...
        'Press a botton to proceed.'
        ];
    % flicker method to get isoluminant stimuli
    design.isolumInstruction = ['LUMINANCE ADJUSTMENT\n\n'...
        'In the following task\n' ...
        'you will be shown a series of \n' ...
        'colored flickering color patches.\n'...
        'The goal of this task is to change \n' ...
        'the luminance of each patch such that the\n'...
        'flickering is minimized\n' ... 
        'using the "4" and "6" button.\n\n'...
        'If you think that minimal flickering\n' ... 
        'has been achieved, press the "5" button\n'...
        'This task is repeated 15 times and takes about 5 minutes.\n\n'...
        'PRESS THE "5" KEY TO PROCEED'];
    
    % BR grating experiment
    design.InstructionGratings1 = [
        'In the following task you will be shown\n' ...
        'colored gratings to both of your eyes at the same time.\n' ...
        'The gratings have different colors and are orthogonal to each other\n' ...
        'Your perception will alternate between the two gratings.\n' ...
        'Your task will be to indicate which grating you perceive, by pressing corresponding buttons\n' ...
        'Please keep the button pressed, as long as you perceive the corresponding grating.\n\n' ...
        'If yo perceive a mixture of the gratings, DO NOT press ANY button\n\n' ...
        'Press any button to proceed'
        ];
    design.InstructionGratings2 = [
        'In each run you will see ' num2str(design.numTrials) 'grating pairs (' num2str(design.numTrials) 'trials)\n' ...
        'Each pair of gratings is presented for ' round(num2str(design.stimulusPresentationTime)) 's\n' ... 
        'Between the trials there is a ' round(num2str(design.ITI)) 's break.\n\n' ...
        'Press any key to proceed'
        ];
    design.InstructionGratings3 = [
        'Reminder:\n\n'
        num2str(design.numTrials) ' pairs of gratings (trials)\n' ...
        'Each pair of gratings will be presented for ' num2str(round(design.stimulusPresentationTime)) 's\n' ...
        'Between trials there is a ' num2str(round(design.ITI)) 's break.\n\n' ...
        'When you perceive one grating dominantly, keep the corresponding button pressed.\n' ...
        'Try to stay focused during the entire experiment.\n\n' ...
        'Press any key to proceed'];
    design.trueExampleText = 'When you see this\n pattern, please press 4';
    design.invExampleText = 'When you see this\n pattern, please press 6';
    design.proceedText = 'Press any button to proceed';
    
    % BR experiment
    design.Instruction1 = [
        'In the following you will be presented with objects in their typical\n' ...
        'and untypical color to both of your eyes at the same time.\n' ...
        'Your perception will alternate between the two colors.\n' ...
        'When you see the object in its typical color, please press left (4)\n' ...
        'when you see the object in an untypical color, please press right (6).\n' ...
        'Should you see a mixture of both colors or parts of the object in one color and\n' ...
        'other parts of the object in a different color, please do NOT press ANY button.\n\n' ...
        'Press any button to continue'
        ];
    design.Instruction2 = [
        'In each run you will be presented with ' num2str(design.numTrials) ' different objects.\n' ...
        'Each object will be shown for ' num2str(round(design.stimulusPresentationTime)) 's.\n' ...
        'Between the trials there will be a break of ' num2str(round(design.ITI)) 's.\n\n' ...
        'Try to stay focused during the whole experiment\n\n' ...
        'Press any button to continue'
        ];
    design.Instruction3 = [
        'Reminder:\n\n' ...
        'Typical color - LEFT (4)\n' ...
        'Untypical color - RIGHT (6)\n' ...
        num2str(round(design.stimulusPresentationTime)) 's per object, ' num2str(round(design.ITI)) 's break between objects.\n\n' ...
        'Press any button to continue'
        ];

    design.waitTillStart = [
        'We start in ' num2str(round(design.ITI)) 's.\n\n' 
        ];

    design.FusionText   = 'Fusion test: use arrows to adjust or press "ENTER" to continue';
    design.RunIsOver    = [
        'This run is now over\n\n' ...
        'Press any button to continue'
        ];
end
end
